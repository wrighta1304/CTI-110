import turtle
chase = turtle.Turtle()
chase.color('green')
chase.pensize(5)
chase.shape('turtle')

chase.forward(100)
chase.left(90)
chase.forward(100)
chase.right(90)

chase.backward(100)
chase.right(90)
chase.forward(100)
chase.left(90)

